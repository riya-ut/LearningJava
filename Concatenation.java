public class Concatenation {

    public static void main(String[] args) {
        System.out.println("Java".concat("JavaScript"));
        String s1 = "HTML";
        String s2 = "CSS";

        System.out.println(s1.concat(s2));

        String s3 = "5";
        String s4 = "10";

        System.out.println(s3.concat(s4));
    }

}
